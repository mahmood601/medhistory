export const getSeasonData = (): ChartData => {
  let index = 0,
    labels: string[] = [],
    seasonArray: number[] = []


  chartArr.forEach((season, seasonIndex) => {
    season.forEach((section, sectionIndex) => {
      const isSpecialSeason = seasonIndex === 0 || seasonIndex === 6;
      // don't add 1 to seasonIndex because 0 will not considered as season it's a road
      const label = isSpecialSeason ? 'طريق عبور' : `الفصل ${seasonIndex} المبحث ${sectionIndex + 1}`;
      labels.push(label);
      seasonArray.push(section.difficulty);
      index++;
    });
  });

  const datasets = [{
    data: seasonArray,
    borderColor: "#693800",
    tension: 0.3,
    label: 'مستوى الصعوبة',
    pointRadius: 10,

  }];
  console.log(datasets);


  return { labels, datasets };
}

